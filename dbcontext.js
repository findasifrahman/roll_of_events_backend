const Sequelize = require('sequelize');


const sequelize = new Sequelize('dnoarchieve', 'postgres', 'asif10018', {
  host: 'localhost',
  dialect: 'postgres',
  pool: {
    max: 5,
    min: 0,
    acquire: 30000,
    idle: 10000
  },
  define: {
    timestamps: false,
    freezeTableName: true
  }
});

